<template>
  <div>
    <div class="div-events">
      <el-button @click="isShowEvent = true">添加事件</el-button>
      <div>
        <el-tag
          v-for="event in Object.keys(componentDataStore.curComponent.events)"
          :key="event"
          closable
          @close="() => eventStore.deleteEvent(event)"
        >
          {{ event }}
        </el-tag>
      </div>
    </div>

    <!-- 选择事件 -->
    <Modal v-model="isShowEvent">
      <el-tabs v-model="eventActiveName">
        <el-tab-pane
          v-for="item in eventListRef"
          :key="item.key"
          :label="item.label"
          :name="item.key"
          style="padding: 0 20px;"
        >
          <el-input
            v-if="item.key == 'redirect'"
            v-model="item.param"
            type="textarea"
            placeholder="请输入完整的 URL"
          />
          <el-input
            v-if="item.key == 'alert'"
            v-model="item.param"
            type="textarea"
            placeholder="请输入要 alert 的内容"
          />
          <el-button
            style="margin-top: 20px;"
            @click="confirm(item.key, item.param)"
            >确定</el-button
          >
        </el-tab-pane>
      </el-tabs>
    </Modal>
  </div>
</template>

<script setup lang="ts">
import useComponentData from "@/stores";
import useEventData from "@/stores/event";
import { eventList } from "@/utils/eventList";
import Modal from "./Modal.vue";
import { ref } from "vue";
const componentDataStore = useComponentData();
const eventStore = useEventData();
const isShowEvent = ref(false);
const eventActiveName = ref("redirect");
const eventListRef = ref(eventList);
const confirm = (eventName: string, eventParam: string) => {
  eventStore.addEvent(eventName, eventParam);
  isShowEvent.value = false;
};
</script>

<style lang="scss" scoped>
.div-events {
  text-align: center;
  padding: 0 20px;

  .el-button {
    display: inline-block;
    margin-bottom: 10px;
  }

  .el-tag {
    display: block;
    width: 50%;
    margin: auto;
    margin-bottom: 10px;
  }
}
</style>
